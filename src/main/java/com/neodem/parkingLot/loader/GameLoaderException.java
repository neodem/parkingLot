package com.neodem.parkingLot.loader;

public class GameLoaderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public GameLoaderException() {
	}

	public GameLoaderException(String arg0) {
		super(arg0);
	}

	public GameLoaderException(Throwable arg0) {
		super(arg0);
	}

	public GameLoaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
